    /// <summary>
    /// Rotates the 4-byte IV in place using a fixed-seed key schedule and
    /// the <see cref="Shuffle"/> table. Call exactly once per packet (both
    /// directions) immediately after the body has been
    /// encrypted/decrypted, BEFORE the next packet starts.
    /// </summary>
    /// <param name="source">The 4-byte IV to rotate. Mutated in place.</param>
    /// <exception cref="ArgumentException"><paramref name="source"/> must be exactly 4 bytes.</exception>
    public static void InnoHash(Span<byte> source)
    {
        if (source.Length != 4)
        {
            throw new ArgumentException("source must be exactly 4 bytes", nameof(source));
        }

        ReadOnlySpan<byte> shuffle = Shuffle;
        Span<int> key = stackalloc int[4];
        key[0] = 0xF2;
        key[1] = 0x53;
        key[2] = 0x50;
        key[3] = 0xC6;

        unchecked
        {
            for (var i = 0; i < source.Length; i++)
            {
                var a = source[i] & 0xFF;
                key[0] += shuffle[key[1]] - a;
                key[1] -= key[2] ^ shuffle[a];
                key[2] ^= shuffle[key[3]] + a;
                key[3] -= key[0] - shuffle[a];

                var b = ((uint)key[0] & 0xFFu)
                      | (((uint)key[1] << 8) & 0xFF00u)
                      | (((uint)key[2] << 16) & 0xFF0000u)
                      | (((uint)key[3] << 24) & 0xFF000000u);
                var c = (b << 3) | (b >> 29);

                key[0] = (int)(c & 0xFFu);
                key[1] = (int)((c >> 8) & 0xFFu);
                key[2] = (int)((c >> 16) & 0xFFu);
                key[3] = (int)((c >> 24) & 0xFFu);
            }
        }

        source[0] = (byte)key[0];
        source[1] = (byte)key[1];
        source[2] = (byte)key[2];
        source[3] = (byte)key[3];
    }
}
