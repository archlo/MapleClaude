namespace MapleClaude.Wz;

/// <summary>
/// Constants for the GMS v95 WZ file format. Byte-identical to the upstream
/// Kinoko <c>WzConstants.java</c>.
/// </summary>
internal static class WzConstants
{
    /// <summary>
    /// The 128-byte expanded AES user key used to derive the WZ string cipher.
    /// Only every 16th byte carries real entropy; the other 112 bytes are
    /// padding zeros. <see cref="WzCrypto"/> compresses this down to a 32-byte
    /// key (with 24 trailing zeros) for AES-256-CBC.
    /// </summary>
    public static readonly byte[] AesUserKey =
    [
        0x13, 0x00, 0x00, 0x00, 0x52, 0x00, 0x00, 0x00, 0x2A, 0x00, 0x00, 0x00, 0x5B, 0x00, 0x00, 0x00,
        0x08, 0x00, 0x00, 0x00, 0x02, 0x00, 0x00, 0x00, 0x10, 0x00, 0x00, 0x00, 0x60, 0x00, 0x00, 0x00,
        0x06, 0x00, 0x00, 0x00, 0x02, 0x00, 0x00, 0x00, 0x43, 0x00, 0x00, 0x00, 0x0F, 0x00, 0x00, 0x00,
        0xB4, 0x00, 0x00, 0x00, 0x4B, 0x00, 0x00, 0x00, 0x35, 0x00, 0x00, 0x00, 0x05, 0x00, 0x00, 0x00,
        0x1B, 0x00, 0x00, 0x00, 0x0A, 0x00, 0x00, 0x00, 0x5F, 0x00, 0x00, 0x00, 0x09, 0x00, 0x00, 0x00,
        0x0F, 0x00, 0x00, 0x00, 0x50, 0x00, 0x00, 0x00, 0x0C, 0x00, 0x00, 0x00, 0x1B, 0x00, 0x00, 0x00,
        0x33, 0x00, 0x00, 0x00, 0x55, 0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x09, 0x00, 0x00, 0x00,
        0x52, 0x00, 0x00, 0x00, 0xDE, 0x00, 0x00, 0x00, 0xC7, 0x00, 0x00, 0x00, 0x1E, 0x00, 0x00, 0x00,
    ];

    /// <summary>GMS region IV (the one v95 GMS WZ files use).</summary>
    public static readonly byte[] WzGmsIv = [0x4D, 0x23, 0xC7, 0x2B];

    /// <summary>Empty IV — signals "no string encryption" (some early WZ versions).</summary>
    public static readonly byte[] WzEmptyIv = new byte[4];

    /// <summary>Magic constant used in the offset descrambler.</summary>
    public const uint WzOffsetConstant = 0x581C3F6D;
}
